var x = 10;
x += 5; // x = x+5
x *= 2; // x = x*2
console.log(x);
var y = 10;
y -= 5; // y = y-5
y /= 5; // y = y/5
console.log(y);

console.log(15 % 4); // 나누기 연산자
console.log(3 ** 3); // 제곱 연산자
console.log(+10); // 양수 10
console.log(-10); // 음수 10
var value = 10;
++value; // value = value +1
--value; // value = value -1
console.log(value);
